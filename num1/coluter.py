a=int(input("enter the num="))
b=int(input("enter the num"))
symbol=input("enter the symbol")
if symbol=="+":
    print(a+b)
elif symbol=="-":
    print(a-b)
elif symbol=="*":
    print(a*b)
elif symbol=="/":
    print(a/b)
elif symbol=="%":
    print(a%b)
elif symbol=="//":
    print(a//b)
else:
    print("nothing")